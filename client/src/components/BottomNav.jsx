export default function BottomNav() {
  const { user } = useAuth();
  const { pathname } = useLocation();
  const isManager = user?.role === 'manager' || user?.role === 'admin';

  const item = (to, label, Icon) => (
    <Link key={to} to={to} className={`bottom-nav-item ${pathname === to ? 'active' : ''}`}>
      <Icon />
      <span>{label}</span>
    </Link>
  );

  return (
    <nav className="bottom-nav">
      <div className="bottom-nav-items">
        {item('/', 'Home', HomeIcon)}
        {item('/trips', 'Trips', ListIcon)}
        {item('/trip/active', 'Active', MapPinIcon)}
        {isManager && item('/manager', 'Manager', LayoutIcon)}
        {isManager && item('/live-map', 'Live Map', RadioIcon)}
        {item('/profile', 'Profile', UserIcon)}
      </div>
    </nav>
  );
}
